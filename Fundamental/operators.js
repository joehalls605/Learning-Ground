// Assignment operators


// Initialize variables
let x = 5;
let y = 10;
let z = 20;

// Basic assignment
let a = x; // a = 5

// Addition assignment
y += 5; // y = 15

// Subtraction assignment
z -= 7; // z = 13

// Multiplication assignment
let b = 4;
b *= 3; // b = 12

// Division assignment
let c = 24;
c /= 6; // c = 4

// Remainder assignment
let d = 17;
d %= 5; // d = 2

// Exponentiation assignment
let e = 3;
e **= 2; // e = 9

console.log("Basic assignment: " + a);
console.log("Addition assignment: " + y);
console.log("Subtraction assignment: " + z);
console.log("Multiplication assignment: " + b);
console.log("Division assignment: " + c);
console.log("Remainder assignment: " + d);
console.log("Exponentiation assignment: " + e);

