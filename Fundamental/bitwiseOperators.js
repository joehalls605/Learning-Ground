// Bitwise AND (&)
let resultAND = 5 & 3; // 0101 & 0011 = 0001 (equals 1 in decimal)
console.log("Bitwise AND:", resultAND); // Outputs: 1

// Bitwise OR (|)
let resultOR = 5 | 3; // 0101 | 0011 = 0111 (equals 7 in decimal)
console.log("Bitwise OR:", resultOR); // Outputs: 7

// Bitwise XOR (^)
let resultXOR = 5 ^ 3; // 0101 ^ 0011 = 0110 (equals 6 in decimal)
console.log("Bitwise XOR:", resultXOR); // Outputs: 6

// Bitwise NOT (~)
let resultNOT = ~5; // ~0101 = 1010 (equals -6 in decimal)
console.log("Bitwise NOT:", resultNOT); // Outputs: -6

// Left Shift (<<)
let resultLeftShift = 5 << 1; // 0101 << 1 = 1010 (equals 10 in decimal)
console.log("Left Shift:", resultLeftShift); // Outputs: 10

// Right Shift (>>)
let resultRightShift = 5 >> 1; // 0101 >> 1 = 0010 (equals 2 in decimal)
console.log("Right Shift:", resultRightShift); // Outputs: 2
