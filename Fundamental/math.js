/*
The Math object in JavaScript is a built-in object that has properties and methods for mathematical constants and functions. Unlike other global objects, Math is not a constructor, so all properties and methods are static and can be called directly.
*/

// Math.random() generates a floating point number between 0 (inclusive) and 1(exclusive)

let randomNum = Math.random();
console.log(randomNum);

// Math.floor rounds a number down to the nearerst integer.

